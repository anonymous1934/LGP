class PatchEmbed(nn.Module):
    """ 2D Image to Patch Embedding
    """
    def __init__(self, img_size=224, patch_size=16, stride=16, in_chans=3, embed_dim=768, norm_layer=None, flatten=True):
        super().__init__()
        img_size = to_2tuple(img_size)
        patch_size = to_2tuple(patch_size)
        self.img_size = img_size
        self.patch_size = patch_size
        self.grid_size = ((img_size[0] - patch_size[0]) // stride + 1, (img_size[1] - patch_size[1]) // stride + 1)
        self.num_patches = self.grid_size[0] * self.grid_size[1]
        self.flatten = flatten

        self.proj = nn.Conv2d(in_chans, embed_dim, kernel_size=patch_size, stride=stride)
        self.norm = norm_layer(embed_dim) if norm_layer else nn.Identity()

    def forward(self, x):
        B, C, H, W = x.shape
        assert H == self.img_size[0] and W == self.img_size[1], \
            f"Input image size ({H}*{W}) doesn't match model ({self.img_size[0]}*{self.img_size[1]})."
        x = self.proj(x)
        if self.flatten:
            x = x.flatten(2).transpose(1, 2)  # BCHW -> BNC
        x = self.norm(x)
        return x
    

class Block(nn.Module):
    def __init__(
        self, dim, mixer_cls, norm_cls=nn.LayerNorm, fused_add_norm=False, residual_in_fp32=False,drop_path=0.,
    ):
        """
        Simple block wrapping a mixer class with LayerNorm/RMSNorm and residual connection"

        This Block has a slightly different structure compared to a regular
        prenorm Transformer block.
        The standard block is: LN -> MHA/MLP -> Add.
        [Ref: https://arxiv.org/abs/2002.04745]
        Here we have: Add -> LN -> Mixer, returning both
        the hidden_states (output of the mixer) and the residual.
        This is purely for performance reasons, as we can fuse add and LayerNorm.
        The residual needs to be provided (except for the very first block).
        """
        super().__init__()
        self.residual_in_fp32 = residual_in_fp32
        self.fused_add_norm = fused_add_norm
        # import ipdb; ipdb.set_trace()
        self.mixer = mixer_cls(dim)
        self.norm = norm_cls(dim)
        self.drop_path = DropPath(drop_path) if drop_path > 0. else nn.Identity()
        if self.fused_add_norm:
            assert RMSNorm is not None, "RMSNorm import fails"
            assert isinstance(
                self.norm, (nn.LayerNorm, RMSNorm)
            ), "Only LayerNorm and RMSNorm are supported for fused_add_norm"

    def forward(
        self, hidden_states: Tensor, residual: Optional[Tensor] = None, inference_params=None
    ):
        r"""Pass the input through the encoder layer.

        Args:
            hidden_states: the sequence to the encoder layer (required).
            residual: hidden_states = Mixer(LN(residual))
        """
        if not self.fused_add_norm:
            if residual is None:
                residual = hidden_states
            else:
                residual = residual + self.drop_path(hidden_states)
            
            hidden_states = self.norm(residual.to(dtype=self.norm.weight.dtype))
            if self.residual_in_fp32:
                residual = residual.to(torch.float32)
        else:
            fused_add_norm_fn = rms_norm_fn if isinstance(self.norm, RMSNorm) else layer_norm_fn
            if residual is None:
                hidden_states, residual = fused_add_norm_fn(
                    hidden_states,
                    self.norm.weight,
                    self.norm.bias,
                    residual=residual,
                    prenorm=True,
                    residual_in_fp32=self.residual_in_fp32,
                    eps=self.norm.eps,
                )
            else:
                hidden_states, residual = fused_add_norm_fn(
                    self.drop_path(hidden_states),
                    self.norm.weight,
                    self.norm.bias,
                    residual=residual,
                    prenorm=True,
                    residual_in_fp32=self.residual_in_fp32,
                    eps=self.norm.eps,
                )    
        hidden_states = self.mixer(hidden_states, inference_params=inference_params)
        return hidden_states, residual

    def allocate_inference_cache(self, batch_size, max_seqlen, dtype=None, **kwargs):
        return self.mixer.allocate_inference_cache(batch_size, max_seqlen, dtype=dtype, **kwargs)


def create_block(
    d_model,
    d_state=16,
    ssm_cfg=None,
    norm_epsilon=1e-5,
    drop_path=0.,
    rms_norm=False,
    residual_in_fp32=False,
    fused_add_norm=False,
    layer_idx=None,
    device=None,
    dtype=None,
    if_bimamba=False,
    bimamba_type="none",
    if_divide_out=False,
    init_layer_scale=None,
):
    if if_bimamba:
        bimamba_type = "v1"
    if ssm_cfg is None:
        ssm_cfg = {}
    factory_kwargs = {"device": device, "dtype": dtype}
    # import ipdb; ipdb.set_trace()
    mixer_cls = partial(Mamba, d_state=d_state, layer_idx=layer_idx, bimamba_type=bimamba_type, if_divide_out=if_divide_out, init_layer_scale=init_layer_scale, **ssm_cfg, **factory_kwargs)
    norm_cls = partial(
        nn.LayerNorm if not rms_norm else RMSNorm, eps=norm_epsilon, **factory_kwargs
    )
    block = Block(
        d_model,
        mixer_cls,
        norm_cls=norm_cls,
        drop_path=drop_path,
        fused_add_norm=fused_add_norm,
        residual_in_fp32=residual_in_fp32,
    )
    block.layer_idx = layer_idx
    return block


# https://github.com/huggingface/transformers/blob/c28d04e9e252a1a099944e325685f14d242ecdcd/src/transformers/models/gpt2/modeling_gpt2.py#L454
def _init_weights(
    module,
    n_layer,
    initializer_range=0.02,  # Now only used for embedding layer.
    rescale_prenorm_residual=True,
    n_residuals_per_layer=1,  # Change to 2 if we have MLP
):
    if isinstance(module, nn.Linear):
        if module.bias is not None:
            if not getattr(module.bias, "_no_reinit", False):
                nn.init.zeros_(module.bias)
    elif isinstance(module, nn.Embedding):
        nn.init.normal_(module.weight, std=initializer_range)

    if rescale_prenorm_residual:
        # Reinitialize selected weights subject to the OpenAI GPT-2 Paper Scheme:
        #   > A modified initialization which accounts for the accumulation on the residual path with model depth. Scale
        #   > the weights of residual layers at initialization by a factor of 1/√N where N is the # of residual layers.
        #   >   -- GPT-2 :: https://openai.com/blog/better-language-models/
        #
        # Reference (Megatron-LM): https://github.com/NVIDIA/Megatron-LM/blob/main/megatron/model/gpt_model.py
        for name, p in module.named_parameters():
            if name in ["out_proj.weight", "fc2.weight"]:
                # Special Scaled Initialization --> There are 2 Layer Norms per Transformer Block
                # Following Pytorch init, except scale by 1/sqrt(2 * n_layer)
                # We need to reinit p since this code could be called multiple times
                # Having just p *= scale would repeatedly scale it down
                nn.init.kaiming_uniform_(p, a=math.sqrt(5))
                with torch.no_grad():
                    p /= math.sqrt(n_residuals_per_layer * n_layer)


def segm_init_weights(m):
    if isinstance(m, nn.Linear):
        trunc_normal_(m.weight, std=0.02)
        if isinstance(m, nn.Linear) and m.bias is not None:
            nn.init.constant_(m.bias, 0)
    elif isinstance(m, nn.Conv2d):
        # NOTE conv was left to pytorch default in my original init
        lecun_normal_(m.weight)
        if m.bias is not None:
            nn.init.zeros_(m.bias)
    elif isinstance(m, (nn.LayerNorm, nn.GroupNorm, nn.BatchNorm2d)):
        nn.init.zeros_(m.bias)
        nn.init.ones_(m.weight)

class DepthwiseSeparableConv(nn.Module):
    def __init__(self, in_channels, out_channels, kernel_size=3, padding=1):
        super(DepthwiseSeparableConv, self).__init__()
        # 深度卷积：在每个通道上独立进行卷积
        self.depthwise = nn.Conv2d(in_channels, in_channels, kernel_size=kernel_size, padding=padding, groups=in_channels)
        # 逐点卷积：通过 1x1 卷积进行通道间融合
        self.pointwise = nn.Conv2d(in_channels, out_channels, kernel_size=1)

    def forward(self, x):
        x = self.depthwise(x)
        x = self.pointwise(x)
        return x

class AdaptiveFusionNet(nn.Module):
    def __init__(self, embed_dim=96, patch_size=56):
        super(AdaptiveFusionNet, self).__init__()
        self.conv1 = DepthwiseSeparableConv(embed_dim, embed_dim)
        self.conv2 = DepthwiseSeparableConv(embed_dim, embed_dim)
        self.conv3 = DepthwiseSeparableConv(embed_dim, embed_dim)
        #self.wavelet_positions = nn.Parameter(torch.randn((embed_dim, patch_size, patch_size)))

        # 初始化 wavelet_positions
        #trunc_normal_(self.wavelet_positions, std=.02)

        # 定义可学习的权重参数
        self.weight_local = nn.Parameter(torch.tensor(0.5))  # 对局部特征的权重
        self.weight_global = nn.Parameter(torch.tensor(0.5))  # 对全局特征的权重

    def forward(self, x, x_global):
        # 使用可学习的权重对局部和全局特征进行融合
        x_fused = self.weight_local * x + self.weight_global * x_global

        # 通过深度可分离卷积处理融合特征
        x_fused = torch.relu(self.conv1(x_fused))
        x_fused = torch.relu(self.conv2(x_fused))
        x_fused = x + x_fused
        x_fused = self.conv3(x_fused)

        # 残差连接
        x_out = x_fused

        return x_out

class WaveletEmbedding(nn.Module):
    """
    模块化的小波嵌入模块，可即插即用到其他Mamba变种中
    
    Args:
        embed_dim: 嵌入维度
        img_size: 输入图像尺寸
        patch_size: patch大小
        wavelet_name: 小波函数名称
        wavelet_levels: 小波分解层数
        use_fusion: 是否使用自适应融合
        device: 设备
    """
    def __init__(self, 
                 embed_dim=192, 
                 img_size=224, 
                 patch_size=16,
                 wavelet_name="haar",
                 wavelet_levels=4,
                 use_fusion=True,
                 device=None):
        super(WaveletEmbedding, self).__init__()
        
        self.embed_dim = embed_dim
        self.img_size = img_size
        self.patch_size = patch_size
        self.wavelet_levels = wavelet_levels
        self.use_fusion = use_fusion
        self.device = device or torch.device('cuda' if torch.cuda.is_available() else 'cpu')
        
        # 计算小波变换后的通道数
        # 每层小波变换产生4个子带（1个低频 + 3个高频）
        total_channels = 3 * (4 ** wavelet_levels)  # 3是RGB通道数
        
        # 全局特征提取卷积
        self.global_conv = nn.Conv2d(total_channels, embed_dim, kernel_size=1, stride=1, padding=0)
        
        # 初始化小波变换函数
        self.dwtfunction = []
        max_transforms = sum(4**i for i in range(wavelet_levels))  # 计算所需的变换函数数量
        for i in range(max_transforms):
            dwt = DWTForward(J=1, mode='periodization', wave=wavelet_name).to(self.device)
            self.dwtfunction.append(dwt)
        
        # 自适应融合网络
        if self.use_fusion:
            grid_size = img_size // patch_size
            self.fusion = AdaptiveFusionNet(embed_dim=embed_dim, patch_size=grid_size)
        
    def _apply_wavelet_transform(self, x):
        """
        应用多层小波变换
        
        Args:
            x: 输入张量 [B, C, H, W]
        
        Returns:
            变换后的特征张量
        """
        result1 = [x]
        num = 0
        
        for level in range(self.wavelet_levels):
            result2 = []
            for dwtx in result1:
                yl, yh = self.dwtfunction[num](dwtx)
                result2.append(yl)
                result2.append(yh[0][:, :, 0])
                result2.append(yh[0][:, :, 1])  
                result2.append(yh[0][:, :, 2])
                num += 1
            result1 = result2
        
        # 连接所有特征
        result = result1[0]
        for i in range(1, len(result1)):
            result = torch.cat((result, result1[i]), 1)
            
        return result
    
    def forward(self, x, patch_embeddings=None):
        """
        前向传播
        
        Args:
            x: 原始输入图像 [B, C, H, W]
            patch_embeddings: 已有的patch嵌入 [B, N, D] (可选)
        
        Returns:
            如果use_fusion=True且提供了patch_embeddings: 融合后的嵌入
            否则: 全局小波特征 [B, embed_dim, H', W']
        """
        # 应用小波变换
        wavelet_features = self._apply_wavelet_transform(x)
        
        # 提取全局特征
        global_features = self.global_conv(wavelet_features)
        
        # 如果不使用融合或没有提供patch嵌入，直接返回全局特征
        if not self.use_fusion or patch_embeddings is None:
            return global_features
        
        # 融合局部和全局特征
        B, N, D = patch_embeddings.shape
        grid_size = int((N - 1) ** 0.5) if N > 1 else int(N ** 0.5)  # 考虑cls token
        
        # 处理patch嵌入形状
        x_patches = patch_embeddings.permute(0, 2, 1)
        
        # 分离cls token（如果存在）
        if N == grid_size * grid_size + 1:  # 有cls token
            x_final = x_patches[:, :, -1:]
            x_patches = x_patches[:, :, :-1].reshape(B, D, grid_size, grid_size)
        else:
            x_final = None
            x_patches = x_patches.reshape(B, D, grid_size, grid_size)
        
        # 融合特征
        fused_features = x_patches + self.fusion(x_patches, global_features)
        
        # 重新组织形状
        fused_features = fused_features.reshape(B, D, -1)
        if x_final is not None:
            fused_features = torch.cat((fused_features, x_final), dim=-1)
        
        return fused_features.permute(0, 2, 1)
    
    def get_global_features_only(self, x):
        """仅获取全局小波特征，不进行融合"""
        wavelet_features = self._apply_wavelet_transform(x)
        return self.global_conv(wavelet_features)

